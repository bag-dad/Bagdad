<p>Welcome to the index controller.</p>

<h2>Download</h2>
<p>You can download Bagdad from github.</p>
<blockquote>
<code>git clone https://github.com/bag-dad/Bagdad.git</code>
</blockquote>


<h2>Installation</h2>
<p>First you have to make the data-directory writable. This is the place where Bagdad needs
to be able to write and create files.</p>
<blockquote>
<code>cd bagdad; chmod 777 site/data</code>
</blockquote>

<p>Second, Bagdad has some modules that need to be initialised. You can do this through a
controller. Point your browser to the following link.</p>
<blockquote>
<a href='<?=create_url('module/install')?>'>module/install</a>
</blockquote>
